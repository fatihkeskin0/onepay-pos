const BC_EXPIRY_TZ = "Europe/Istanbul";

export function formatBcExpiry(date: Date): string {
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: BC_EXPIRY_TZ,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: false,
  }).formatToParts(date);

  const get = (type: Intl.DateTimeFormatPartTypes): string =>
    parts.find((p) => p.type === type)?.value ?? "00";

  return `${get("year")}-${get("month")}-${get("day")} ${get("hour")}:${get("minute")}:${get("second")}`;
}
